#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] && s2[i])
	{
		if ((s1[i] - s2[i]) == 0)
		{
			i++;
		}
		else
			break ;
	}
	return (s1[i] - s2[i]);
}
