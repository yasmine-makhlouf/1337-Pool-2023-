#include <unistd.h>

int	main(int ac, char *av[])
{
	int	i;
	int	j;

	i = 1;
	j = 0;
	while (i < ac)
	{
		while (av[i][j])
		{
			write(1, &av[i][j], 1);
			j++;
		}
		i++;
		j = 0;
		write (1, "\n", 1);
	}
	return (0);
}
