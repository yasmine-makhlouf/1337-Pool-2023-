#include <unistd.h>

int	main(int ac, char *av[])
{
	int	j;

	j = 0;
	while (ac > 1)
	{
		while (av[ac - 1][j])
		{
			write (1, &av[ac - 1][j], 1);
			j++;
		}
		write (1, "\n", 1);
		ac--;
		j = 0;
	}
	return (0);
}
